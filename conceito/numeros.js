//Números e suas normalidades!
console.log(2 + 2); //Soma
console.log(2 - 2); //Subtração
console.log(2 * 2); //Multiplicação
console.log(2 / 2); //Divisão
console.log(2 % 2); //Resto

//Números e suas doidices!
console.log(7 / 0) //Infinity
console.log("10" / 2) //Aceito
console.log("10" + 2) //Texto ganha
console.log("texto" * 2) //NaN -> Not a number
console.log(0.1 + 0.7) //Estranho
